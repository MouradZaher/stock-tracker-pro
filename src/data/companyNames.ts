/**
 * Robust helper to retrieve full company name with bracket stripping.
 */
export const getFullCompanyName = (symbol: string, fallbackName?: string): string => {
    if (!symbol) return fallbackName || '';
    
    // Normalize symbol for mapping
    const lookupSymbol = symbol.toUpperCase().trim();
    const shortSymbol = lookupSymbol.split('.')[0].split(':')[0].split('-')[0];
    
    const fullName = COMPANY_NAMES[lookupSymbol] || COMPANY_NAMES[shortSymbol] || fallbackName || symbol;
    
    // Final institutional cleaning: strip all brackets and parentheses
    return fullName.replace(/[()[\]]/g, '').trim();
};
